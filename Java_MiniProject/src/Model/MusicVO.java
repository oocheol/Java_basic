package Model;

public class MusicVO {

	
	private String song;
	private String singer;
	private String path;
	
	public MusicVO(String song, String singer, String path) {
		this.song = song;
		this.singer = singer;
		this.path = path;
	}

	public String getSong() {
		return song;
	}

	public String getSinger() {
		return singer;
	}

	public String getPath() {
		return path;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}

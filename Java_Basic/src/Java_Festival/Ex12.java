package Java_Festival;

import java.util.Scanner;

public class Ex12 {

		public static void main(String args[]) {
			
			String star = "*";
			String blank = " ";
			
			for (int i=1;i<=5;i++) {
				for (int j=4;j>=i;j--) {
					System.out.print(blank);
				}
				for (int k=1;k<=i;k++) {
					System.out.print(star);
				}
				System.out.println();
			}
			
			
			
			
			
			
			
			
			
			
		}

}

